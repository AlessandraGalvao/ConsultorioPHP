<h2>Informacion General</h2>
<p> El <strong>Sistema de gestion odontologica</strong> permite administrar el manejo de la informacion de los pacientes, tratamientos y citas, a traves de una interface amigable
</p>
<p>
	El Sistema de Gestion de Odontologica cuenta con las siguientes secciones.
	<ul>
		<li>Asignar Cita</li>
		<li>Consultar Cita</li>
		<li>Cancelar Cita</li>
	</ul>
</p>
